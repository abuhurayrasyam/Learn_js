let age = 21;

//***//Pre-Increment (++variable): ভেরিয়েবলের মান আগে ১ বৃদ্ধি পায়, তারপর এটি ব্যবহৃত হয়।
console.log(++age); //output: 22

//***//Pre-Decrement (--variable): ভেরিয়েবলের মান আগে ১ কমে, তারপর এটি ব্যবহৃত হয়।
console.log(--age); //output: 21

//***//Post-Increment (variable++): ভেরিয়েবলের বর্তমান মান আগে ব্যবহৃত হয়, তারপর ১ বৃদ্ধি পায়।
console.log(age++); //output: 21
console.log(age); //output: 22

//***//Post-Decrement (variable--): ভেরিয়েবলের বর্তমান মান আগে ব্যবহৃত হয়, তারপর ১ কমে।
console.log(age--); //output: 22
console.log(age); //output: 21