//***//default parameter in function : যখন কোন ফাংশনকে ডিক্লেয়ার করা হয়, তখন ওই ফাংশনের প্যারামিটারের সাথে ডিফল্ট ভ্যালু দিয়ে দিতে হবে। যাতে করে যখনই ওই ফাংশন কে বাইরে থেকে কল করে আর্গুমেন্ট পাস করা হবে, তখন ওই আর্গুমেন্টে এর মধ্যে ভ্যালু না দিলেও আউটপুটে কোন এরর বা NaN রিটার্ন হবে না।
//example:
function sum(num1=0, num2=0){
    return num1 + num2;
}
console.log(sum(10)); //এখানে num1 ভ্যালু পাস করা হয়েছে num2 এর ভ্যালু পাস করা হয়নি।
//output: 10